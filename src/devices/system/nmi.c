#include <stdio.h>
#include <stdint.h>
#include <string.h>
#include <wchar.h>
#include "../../emu.h"
#include "../../io.h"
#include "nmi.h"


int	nmi = 0,
	nmi_auto_clear = 0;
int	nmi_enable = 1;
int	nmi_mask = 0;


static void
nmi_write(uint16_t port, uint8_t val, priv_t priv)
{
    nmi_mask = val & 0x80;
}


void
nmi_init(void)
{
    io_sethandler(0x00a0, 1,
		  NULL,NULL,NULL, nmi_write,NULL,NULL, NULL);

    nmi_mask = 0;
}
